package navalbattle.bonus;


class DamageDueToMine {
    
    // Damaged position
    private final int x;
    private final int y;

    public DamageDueToMine(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
